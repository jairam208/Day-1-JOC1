// SIZE OF DATA TYPES IN C
#include<stdio.h>
int main()
{
    printf("The size of int datatype is    : %d\n", sizeof(int));
    printf("The size of float datatype is  : %d\n", sizeof(float));
    printf("The size of char datatype is   : %d\n", sizeof(char));
    printf("The size of double datatype is : %d\n", sizeof(double));
}